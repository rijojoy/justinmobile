<h2>10 reasons to consult reviews before recycling your iPhone</h2>

<p>Every year Apple releases shiny new tech to the masses - it's thinner, faster and lighter than ever before. You've patiently camped outside the store for hours, tried frantically refreshing your page to pre-order, and called up every mobile network operator to see if the new model in stock. Finally, you manage to get hold of the latest iPhone. Well done you! Now what to do with the old one?</p>

<p>If you want to sell your old iPhone hassle free, you may look to a mobile phone recycling service. But sometimes what seems like a dream can turn into a nightmare and you're left frustrated and out of pocket. That's where iPhone recycling reviews come in! Here are 5 reasons to consult reviews before recycling your iPhone...</p>

<h3>1) Is the company real?</h3>

<p>So, you've found a website. But that doesn't in itself mean the 'company' behind it is legit.</p>

<p>If the reviews appear on a trustworthy third party website such as <a href="https://www.trustpilot.co.uk/review/www.geckomobilerecycling.co.uk" target="_blank">TrustPilot</a> then there is a good chance the company is genuine. However, do take note of the date the last review was left; if the reviews are months old it might be worth giving the company a ring to make sure they're still trading. Another tip is to check their social media feeds to see if they've posted anything recently. You want to be sure someone will actually be there to collect your old iPhone once you've posted it!</p>

<h3>2) Does the iPhone recycler downgrade?</h3>

<p>The mobile recycling industry is notorious for cowboy operators who lure you in with unrealistic prices only to lower their offer once your iPhone arrives - often for fabricated or seemingly pedantic reasons such as 'screen burn' or 'scratches on the body'. While most iPhone recycling services will downgrade heavily damaged handsets, many people would agree that is it unfair to systematically downgrade customers' offers based on minor cosmetic wear and tear. Most people will flag up unfair downgrades in a review - so it's worth reading them before registering your sale.</p>

<h3>3) Does the company charge for returns?</h3>

<p>If the value of your iPhone is lowered after inspection, you should be given the option to have your handset returned to you. There is no law against charging the customer for the downgraded handset to be returned; however, especially when underhand tactics have been used (see above), it can leave a bad taste in the mouth. That is why it's worth scouring iPhone recycling reviews before selling your iPhone, as users disgruntled by return charges will often leave a negative review.</p>

<h3>4) Does the iPhone recycling service pay on time?</h3>

<p>You don't want to be waiting around for weeks for that sweet cash to reach your account. Just because an iPhone recycler offers same day pay doesn't mean they'll deliver. To avoid being left high and dry without that moolah it is well worth checking out the recyclers reviews to ensure that cash will be with you when they say it will.</p> 

<h3>5) Overall, does the service deliver what it promises?</h3>

<p>Words are cheap; actions matter. Before using a mobile recycling service you haven't heard of or used before, do a bit of Googling to find out whether you're likely to be left disappointed. It only takes a few minutes and could save you a lot of stress further down the line.</p>

