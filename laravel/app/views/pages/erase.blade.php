<h1>How to remove Apple's Activation Lock</h1>
<p>
	You must remove Apple's Activation Lock before sending a used iPhone or iPad to us.
</p> 
<h3>If your device is working...</h3>
<p>
Erase all content and remove Apple's Activation Lock by choosing <b>Settings > General > Reset > Erase All Content and Settings</b>. You will need to provide your Apple ID and password.
</p>
<h3>If your device is faulty...</h3>
<p>
If your device is faulty and you are unable to follow the steps above:</p> 
<p> 1) Remove Apple's activation lock by signing in to your iCloud account at <a href="https://www.icloud.com/find" target="_blank">www.icloud.com/find</a>.</p> 
<p>2) Click <b>All Devices</b> to open a list of devices linked to your account, then select the device to be removed.</p>
<p> 3) Click the <b>Erase</b> button to erase all content and settings from your device. When prompted, don't enter a phone number or message. Click <b>Next</b> until your device is erased. </p>
<p>4) When the erase is complete, click <b>"Remove from Account"</b> to remove the device from your account.
</p>