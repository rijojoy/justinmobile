<h2>How mobile recycling came about</h2>

<p><a href="https://www.geckomobilerecycling.co.uk">Mobile recycling</a> has become increasingly more popular over time. A whopping 51% of adults claimed to own a smartphone in 2013! Considering people upgrade every couple of years or so, this has lead to many people being left with an old smartphone but not knowing what to do with it. Hence the burgeoning <b>mobile recycling</b> industry was born.</p>

<h2>What happens when a mobile is recycled?</h2>

</p>Mobile recycling companies do three different things with the mobiles they recycle, depending on the condition and functionality of the device being recycled:</p>

<h3>1) Resell the device</h3>

<p>If the mobile device is in a good condition it may simply be resold either locally or overseas. <b>Mobile recycling</b> companies can often get a better price for your old device than you could yourself, while also making the selling process easier, more reliable and more convenient.</p>

<h3>2) Refurbish the device</h3>

<p>Mobile devices that are in very poor cosmetic condition or have functionality issues such as a broken screen are refurbished before being resold as per the paragraph above. Some <b>mobile recycling</b> companies offer one fixed price for faulty devices, no matter what the issue is. Justin Mobile Recycling offers a minimum amount for your faulty mobile which is almost always increased upon inspection. This is to prevent disappointment - we like to under-promise and over-deliver!

<h3>3) Sell the mobile for parts</h3>

<p>Mobile devices often have salvageable - or valuable - parts, no matter how badly damaged they are overall. When a <b>mobile recycling</b> company receives a device that canâ€™t be refurbished of resold, it will be broken down into its individual parts. These, in turn, will be sold. The salvaged parts could be something as complex as a camera module, or as simple as a gold connector. Given that in the US 70% of landfill heavy metals come from electronics, smelting and recycling the precious metals in smartphones and tablets is especially important.</p>


